public class Map {
    private String name;
    
    /**
     *
     * @param name
     */
    public Map(String name) {
        this.name = name;
    }
    
    /**
     *
     * @return
     */
    public String getName() {
        return name;
    }
    
    /**
     *
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }
}
